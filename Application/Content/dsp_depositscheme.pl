<a href='http://www.depositprotection.com' target='_blank'>http://www.depositprotection.com</a><br/><br/>


The DPS is the only custodial deposit protection scheme, is free to use and open to all
 Landlords and Letting Agents.
 
 The service is funded entirely from the interest earned from deposits held.
 Landlords and Letting Agents will be able to register and make transactions online.
 Paper forms will also be available should internet access be an issue. 
 The scheme is supported by a dedicated call centre and an independent dispute resolution 
 service which can be contacted by calling: 0870 7071 707.
 
 

